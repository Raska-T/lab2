package org.example;

public class Curse {
    private String name;
    private String threatLevel;

    public Curse() {}

    public String getName() {
        return name;
    }

    public String getThreatLevel() {
        return threatLevel;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setThreatLevel(String threatLevel) {
        this.threatLevel = threatLevel;
    }
}
