package org.example;

public class Sorcerer {
    private String name;
    private String rank;

    public Sorcerer() {}

    public String getName() {
        return name;
    }

    public String getRank() {
        return rank;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }
}
